    private static LoadingStrategy[] loadLoadingStrategies() {
        // 这里使用 jdk 的 spi 接口，完成 dubbo "加载策略"对象的动态加载
        return stream(load(LoadingStrategy.class).spliterator(), false)
                .sorted()
                .toArray(LoadingStrategy[]::new);
    }

